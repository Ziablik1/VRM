using Microsoft.AspNetCore.Mvc;
using VRM.BLL.Services;

namespace VRM.Presentation.Controllers
{
    public class SignupController : Controller
    {
        public SignUpper _su;
        public SignupController(SignUpper su)
        {
            _su = su;
        }

        [HttpPost]
        public ActionResult SignUp()
        {
            var u = Request.Form["usrnm"];
            var ml = Request.Form["email"];
            var p = Request.Form["psw"];
            _su.AddNewUser(u,ml,p);
            return View();
        }
    }
}
